using System;
using System.Collections.Generic;
using System.Collections.Immutable;
using System.Text;
using System.Threading;
using Microsoft.CodeAnalysis;
using Microsoft.CodeAnalysis.CSharp.Syntax;
using Microsoft.CodeAnalysis.Text;

namespace Nethermind.JsonRpc.SourceGenerator;

[Generator]
public sealed class RpcJsonTypeInfoGenerator : IIncrementalGenerator
{
    private const string GeneratedSourceHeader = "// <auto-generated/>\n#nullable enable\n";
    private const string JsonRpcNamespace = "Nethermind.JsonRpc";
    private const string JsonRpcModulesNamespace = "Nethermind.JsonRpc.Modules";
    private const string JsonRpcSubscribeNamespace = "Nethermind.JsonRpc.Modules.Subscribe";
    private const string SystemThreadingTasksNamespace = "System.Threading.Tasks";

    private static readonly SymbolDisplayFormat TypeDisplayFormat = new(
        globalNamespaceStyle: SymbolDisplayGlobalNamespaceStyle.Included,
        typeQualificationStyle: SymbolDisplayTypeQualificationStyle.NameAndContainingTypesAndNamespaces,
        genericsOptions: SymbolDisplayGenericsOptions.IncludeTypeParameters,
        miscellaneousOptions: SymbolDisplayMiscellaneousOptions.EscapeKeywordIdentifiers);

    public void Initialize(IncrementalGeneratorInitializationContext context)
    {
        IncrementalValuesProvider<ImmutableArray<string>> rpcTypes = context.SyntaxProvider
            .CreateSyntaxProvider(
                predicate: static (node, _) => node is TypeDeclarationSyntax or InvocationExpressionSyntax,
                transform: static (context, cancellationToken) => GetJsonTypes(context, cancellationToken));

        IncrementalValueProvider<ImmutableArray<ImmutableArray<string>>> collectedTypes = rpcTypes.Collect();
        context.RegisterSourceOutput(collectedTypes, static (context, types) => Generate(context, types));
    }

    private static ImmutableArray<string> GetJsonTypes(GeneratorSyntaxContext context, CancellationToken cancellationToken) =>
        context.Node switch
        {
            TypeDeclarationSyntax typeDeclaration => GetRpcModuleJsonTypes(context, typeDeclaration, cancellationToken),
            InvocationExpressionSyntax invocation => GetSubscriptionJsonTypes(context, invocation, cancellationToken),
            _ => ImmutableArray<string>.Empty
        };

    private static ImmutableArray<string> GetRpcModuleJsonTypes(
        GeneratorSyntaxContext context,
        TypeDeclarationSyntax typeDeclaration,
        CancellationToken cancellationToken)
    {
        if (context.SemanticModel.GetDeclaredSymbol(typeDeclaration, cancellationToken) is not INamedTypeSymbol type ||
            !IsRpcModule(type))
        {
            return ImmutableArray<string>.Empty;
        }

        ImmutableArray<string>.Builder types = ImmutableArray.CreateBuilder<string>();
        AddMethodTypes(type, types);
        for (int i = 0; i < type.AllInterfaces.Length; i++)
        {
            INamedTypeSymbol interfaceType = type.AllInterfaces[i];
            if (IsRpcModule(interfaceType))
            {
                AddMethodTypes(interfaceType, types);
            }
        }

        return types.ToImmutable();
    }

    private static ImmutableArray<string> GetSubscriptionJsonTypes(
        GeneratorSyntaxContext context,
        InvocationExpressionSyntax invocation,
        CancellationToken cancellationToken)
    {
        if (context.SemanticModel.GetSymbolInfo(invocation, cancellationToken).Symbol is not IMethodSymbol method ||
            method.Name != "CreateSubscriptionMessage" ||
            !method.IsGenericMethod ||
            method.TypeArguments.Length != 1 ||
            !IsNamedType(method.ContainingType, JsonRpcSubscribeNamespace, "Subscription"))
        {
            return ImmutableArray<string>.Empty;
        }

        ImmutableArray<string>.Builder types = ImmutableArray.CreateBuilder<string>(1);
        AddJsonType(method.TypeArguments[0], types);
        return types.ToImmutable();
    }

    private static bool IsRpcModule(INamedTypeSymbol? type)
    {
        if (type is null)
        {
            return false;
        }

        for (int i = 0; i < type.AllInterfaces.Length; i++)
        {
            if (IsNamedType(type.AllInterfaces[i], JsonRpcModulesNamespace, "IRpcModule"))
            {
                return true;
            }
        }

        return IsNamedType(type, JsonRpcModulesNamespace, "IRpcModule");
    }

    private static void AddMethodTypes(INamedTypeSymbol type, ImmutableArray<string>.Builder types)
    {
        ImmutableArray<ISymbol> members = type.GetMembers();
        for (int i = 0; i < members.Length; i++)
        {
            if (members[i] is not IMethodSymbol method ||
                method.IsStatic ||
                method.IsGenericMethod ||
                method.IsImplicitlyDeclared ||
                method.MethodKind != MethodKind.Ordinary ||
                method.DeclaredAccessibility != Accessibility.Public)
            {
                continue;
            }

            AddReturnPayloadTypes(method.ReturnType, types);
            for (int j = 0; j < method.Parameters.Length; j++)
            {
                AddJsonType(method.Parameters[j].Type, types);
            }
        }
    }

    private static void AddReturnPayloadTypes(ITypeSymbol returnType, ImmutableArray<string>.Builder types)
    {
        ITypeSymbol resultType = UnwrapTaskLike(returnType);
        if (resultType is not INamedTypeSymbol namedResult || !namedResult.IsGenericType)
        {
            return;
        }

        INamedTypeSymbol resultDefinition = namedResult.ConstructedFrom;
        if (IsNamedType(resultDefinition, JsonRpcNamespace, "ResultWrapper`1"))
        {
            AddJsonType(namedResult.TypeArguments[0], types);
            return;
        }

        if (IsNamedType(resultDefinition, JsonRpcNamespace, "ResultWrapper`2"))
        {
            AddJsonType(namedResult.TypeArguments[0], types);
            AddJsonType(namedResult.TypeArguments[1], types);
        }
    }

    private static ITypeSymbol UnwrapTaskLike(ITypeSymbol type)
    {
        if (type is INamedTypeSymbol namedType &&
            namedType.IsGenericType &&
            (IsNamedType(namedType.ConstructedFrom, SystemThreadingTasksNamespace, "Task`1") ||
             IsNamedType(namedType.ConstructedFrom, SystemThreadingTasksNamespace, "ValueTask`1")))
        {
            return namedType.TypeArguments[0];
        }

        return type;
    }

    private static void AddJsonType(ITypeSymbol type, ImmutableArray<string>.Builder types)
    {
        if (type.TypeKind == TypeKind.TypeParameter || type.SpecialType == SpecialType.System_Void)
        {
            return;
        }

        types.Add(type.ToDisplayString(TypeDisplayFormat));
    }

    private static bool IsNamedType(INamedTypeSymbol type, string namespaceName, string metadataName) =>
        type.ContainingNamespace.ToDisplayString() == namespaceName && type.MetadataName == metadataName;

    private static void Generate(SourceProductionContext context, ImmutableArray<ImmutableArray<string>> typeGroups)
    {
        HashSet<string> uniqueTypes = new(StringComparer.Ordinal);
        for (int i = 0; i < typeGroups.Length; i++)
        {
            ImmutableArray<string> group = typeGroups[i];
            for (int j = 0; j < group.Length; j++)
            {
                uniqueTypes.Add(group[j]);
            }
        }

        if (uniqueTypes.Count == 0)
        {
            return;
        }

        string[] sortedTypes = new string[uniqueTypes.Count];
        uniqueTypes.CopyTo(sortedTypes);
        Array.Sort(sortedTypes, StringComparer.Ordinal);

        StringBuilder builder = new();
        builder.AppendLine(GeneratedSourceHeader);
        builder.AppendLine("using System;");
        builder.AppendLine("using System.Text.Json.Serialization.Metadata;");
        builder.AppendLine("using Nethermind.Serialization.Json;");
        builder.AppendLine();
        builder.AppendLine("namespace Nethermind.JsonRpc;");
        builder.AppendLine();
        builder.AppendLine("internal static partial class GeneratedRpcTypeInfo");
        builder.AppendLine("{");
        builder.AppendLine("    static partial void TryGetCore(Type type, ref JsonTypeInfo? typeInfo)");
        builder.AppendLine("    {");
        for (int i = 0; i < sortedTypes.Length; i++)
        {
            builder.Append("        if (type == typeof(");
            builder.Append(sortedTypes[i]);
            builder.AppendLine("))");
            builder.AppendLine("        {");
            builder.AppendLine("            EthereumJsonSerializer.JsonOptions.TryGetTypeInfo(type, out typeInfo);");
            builder.AppendLine("            return;");
            builder.AppendLine("        }");
            builder.AppendLine();
        }
        builder.AppendLine("    }");
        builder.AppendLine("}");

        context.AddSource("GeneratedRpcJsonContext.g.cs", SourceText.From(builder.ToString(), Encoding.UTF8));
    }
}
