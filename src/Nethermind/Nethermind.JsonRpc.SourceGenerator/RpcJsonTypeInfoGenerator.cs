using System.Collections.Immutable;
using System.Text;
using Microsoft.CodeAnalysis;
using Microsoft.CodeAnalysis.CSharp.Syntax;
using Microsoft.CodeAnalysis.Text;

namespace Nethermind.JsonRpc.SourceGenerator;

[Generator]
public sealed class RpcJsonTypeInfoGenerator : IIncrementalGenerator
{
    private const string GeneratedSourceHeader = "// <auto-generated/>\n#nullable enable\n";

    public void Initialize(IncrementalGeneratorInitializationContext context)
    {
        IncrementalValuesProvider<ImmutableArray<string>> rpcTypes = context.SyntaxProvider
            .CreateSyntaxProvider(
                predicate: static (node, _) => node is TypeDeclarationSyntax or InvocationExpressionSyntax,
                transform: static (context, cancellationToken) => RpcJsonTypeDiscovery.GetJsonTypes(context.SemanticModel, context.Node, cancellationToken));

        IncrementalValuesProvider<ImmutableArray<string>> rpcMethodNames = context.SyntaxProvider
            .CreateSyntaxProvider(
                predicate: static (node, _) => node is TypeDeclarationSyntax,
                transform: static (context, cancellationToken) => RpcJsonTypeDiscovery.GetRpcMethodNames(context.SemanticModel, context.Node, cancellationToken));

        IncrementalValueProvider<ImmutableArray<ImmutableArray<string>>> collectedTypes = rpcTypes.Collect();
        IncrementalValueProvider<ImmutableArray<ImmutableArray<string>>> collectedMethodNames = rpcMethodNames.Collect();
        context.RegisterSourceOutput(collectedTypes.Combine(collectedMethodNames), static (context, values) => Generate(context, values.Left, values.Right));
    }

    private static void Generate(
        SourceProductionContext context,
        ImmutableArray<ImmutableArray<string>> typeGroups,
        ImmutableArray<ImmutableArray<string>> methodNameGroups)
    {
        string[] sortedTypes = RpcJsonTypeDiscovery.GetSortedUniqueTypes(typeGroups);
        string[] sortedMethodNames = RpcJsonTypeDiscovery.GetSortedUniqueTypes(methodNameGroups);

        if (sortedTypes.Length != 0)
        {
            StringBuilder builder = new();
            builder
                .AppendLine(GeneratedSourceHeader)
                .AppendLine("using System.Runtime.CompilerServices;")
                .AppendLine("using System.Text.Json.Serialization.Metadata;")
                .AppendLine("using Nethermind.Serialization.Json;")
                .AppendLine()
                .AppendLine("namespace Nethermind.JsonRpc;")
                .AppendLine()
                .AppendLine("internal static class GeneratedRpcJsonTypeInfoProvider")
                .AppendLine("{")
                .AppendLine("    [ModuleInitializer]")
                .AppendLine("    internal static void Register()")
                .AppendLine("    {")
                .AppendLine("        RpcGeneratedTypeInfoRegistry.Register(RpcTypes, TryGet);");
            for (int i = 0; i < sortedTypes.Length; i++)
            {
                builder.AppendLine($"        RegisterGeneric<{sortedTypes[i]}>();");
            }
            builder
                .AppendLine("    }")
                .AppendLine()
                .AppendLine("    private static readonly System.Type[] RpcTypes =")
                .AppendLine("    {");
            for (int i = 0; i < sortedTypes.Length; i++)
            {
                builder.AppendLine($"        typeof({sortedTypes[i]}),");
            }
            builder
                .AppendLine("    };")
                .AppendLine()
                .AppendLine("    private static JsonTypeInfo? TryGet(System.Type type) =>")
                .AppendLine("        EthereumJsonSerializer.JsonOptions.TryGetTypeInfo(type, out JsonTypeInfo? typeInfo) ? typeInfo : null;")
                .AppendLine()
                .AppendLine("    private static void RegisterGeneric<T>() =>")
                .AppendLine("        RpcGeneratedTypeInfoRegistry.Register(static () =>")
                .AppendLine("            (JsonTypeInfo<T>)EthereumJsonSerializer.JsonOptions.GetTypeInfo(typeof(T)));")
                .AppendLine("}");

            context.AddSource("GeneratedRpcJsonTypeInfoProvider.g.cs", SourceText.From(builder.ToString(), Encoding.UTF8));
        }

        if (sortedMethodNames.Length != 0)
        {
            context.AddSource("GeneratedRpcMethodNamesProvider.g.cs", SourceText.From(GenerateMethodNamesSource(sortedMethodNames), Encoding.UTF8));
        }
    }

    private static string GenerateMethodNamesSource(string[] methodNames)
    {
        StringBuilder builder = new();
        builder
            .AppendLine(GeneratedSourceHeader)
            .AppendLine("using System.Runtime.CompilerServices;")
            .AppendLine()
            .AppendLine("namespace Nethermind.JsonRpc;")
            .AppendLine()
            .AppendLine("internal static class GeneratedRpcMethodNamesProvider")
            .AppendLine("{")
            .AppendLine("    [ModuleInitializer]")
            .AppendLine("    internal static void Register()")
            .AppendLine("    {")
            .AppendLine("        RpcKnownMethodNamesRegistry.Register(RpcMethodNames);")
            .AppendLine("    }")
            .AppendLine()
            .AppendLine("    private static readonly string[] RpcMethodNames =")
            .AppendLine("    {");
        for (int i = 0; i < methodNames.Length; i++)
        {
            builder.AppendLine($"        \"{EscapeStringLiteral(methodNames[i])}\",");
        }
        builder
            .AppendLine("    };")
            .AppendLine("}");
        return builder.ToString();
    }

    private static string EscapeStringLiteral(string value) =>
        value.Replace("\\", "\\\\").Replace("\"", "\\\"");
}
