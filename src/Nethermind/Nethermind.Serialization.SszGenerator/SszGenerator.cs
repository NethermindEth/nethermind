    private static string VarName(string name)
    {
        if (string.IsNullOrEmpty(name)) return name;

        string lowerCased = name.Substring(0, 1).ToLower() + name.Substring(1);

        return lowerCased == "data" || lowerCased == "container" || lowerCased.Contains("offset") ? $"_{lowerCased}" : lowerCased;
    }

    /// <summary>
    /// Emits a `Decode(ReadOnlySequence&lt;byte&gt;, out T)` overload that lets the SSZ-REST
    /// middleware feed a <c>PipeReader</c>'s buffer directly. Single-segment input is
    /// zero-copy; multi-segment consolidates into one pooled buffer before dispatching to
    /// the existing span decoder. (Recursive sequence-aware decode would touch every leaf
    /// primitive — deferred until the bulk-copy shows up as a real cost.)
    /// </summary>
    private static string SequenceDecodeMethod(string typeName) => $@"public static void Decode(System.Buffers.ReadOnlySequence<byte> data, out {typeName} container)
    {{
        if (data.IsSingleSegment)
        {{
            Decode(data.FirstSpan, out container);
            return;
        }}
        int len = (int)data.Length;
        byte[] rented = System.Buffers.ArrayPool<byte>.Shared.Rent(len);
        try
        {{
            System.Buffers.BuffersExtensions.CopyTo(data, rented.AsSpan(0, len));
            Decode(rented.AsSpan(0, len), out container);
        }}
        finally
        {{
            System.Buffers.ArrayPool<byte>.Shared.Return(rented);
        }}
    }}";

    private static string ValidationStatement(SszType decl, SszProperty property, string expression)
    {
        string validation = property.Kind switch
        {
            Kind.Vector when property.Type.Name == "BitArray" => $"ValidateSszBitvectorLength({expression}, {property.Length}, nameof({decl.TypeReferenceName}), nameof({property.Name}));",
            Kind.Vector => $"ValidateSszVectorLength({SpanExpression(property, expression)}, {property.Length}, nameof({decl.TypeReferenceName}), nameof({property.Name}));",
            Kind.List => $"ValidateSszListLimit({SpanExpression(property, expression)}, {property.Limit}UL, nameof({decl.TypeReferenceName}), nameof({property.Name}));",
            Kind.BitVector => $"ValidateSszBitvectorLength({expression}, {property.Length}, nameof({decl.TypeReferenceName}), nameof({property.Name}));",
            Kind.BitList => $"ValidateSszBitlistLimit({expression}, {property.Limit}UL, nameof({decl.TypeReferenceName}), nameof({property.Name}));",
            _ => string.Empty,
        };

        if (!property.IsNullable)
        {
            return validation;
        }

        bool canEncodeNullAsDefault = CanEncodeNullAsDefault(property);

        if (!string.IsNullOrEmpty(validation) && canEncodeNullAsDefault)
        {
            return $"if ({expression} is not null) {validation}";
        }

        if (property.Kind == Kind.Vector)
        {
            return $"if ({expression} is null) throw new System.IO.InvalidDataException(\"Invalid SSZ value for {decl.TypeReferenceName}.{property.Name}: expected {property.Length} elements but found 0.\"); {validation}";
        }

        if (property.IsVariable && !canEncodeNullAsDefault)
        {
            return $"if ({expression} is null) throw new System.IO.InvalidDataException(\"Invalid SSZ value for {decl.TypeReferenceName}.{property.Name}: null variable fields are not decodable.\"); {validation}";
        }

        return validation;
    }

    private static bool CanEncodeNullAsDefault(SszProperty property) =>
        property.Kind switch
        {
            Kind.Vector => property.Type.Kind == Kind.Basic || property.Type.Name == "BitArray",
            Kind.BitVector or Kind.List or Kind.ProgressiveList or Kind.BitList or Kind.ProgressiveBitList => true,
            Kind.Basic when property.Type.HasCustomInlineCodec => true,
            _ => false,
        };

    private static string EncodeValueExpression(SszProperty property, string expression) =>
        property.Kind switch
        {
            Kind.BitList or Kind.ProgressiveBitList => $"{expression} ?? new BitArray(0)",
            _ when property.IsCollection && property.Type.Name != "BitArray" => SpanExpression(property, expression),
            _ => expression,
        };

    private static string EnumSpanExpression(SszProperty property, string expression) =>
        $"System.Runtime.InteropServices.MemoryMarshal.Cast<{property.Type.TypeReferenceName}, {property.Type.EnumType!.TypeReferenceName}>({SpanExpression(property, expression)})";

    private static string SpanExpression(SszProperty property, string expression) =>
        property.IsSpanLikeProperty ? expression
            : property.IsMemoryLikeProperty ? $"{expression}.Span"
            : property.IsArrayProperty ? $"{expression}.AsSpan()"
            : property.HasCollectionAsSpan ? $"{expression}.AsSpan()"
            : $"System.Runtime.InteropServices.CollectionsMarshal.AsSpan({expression})";

    private static IEnumerable<string> ValidationStatements(SszType decl, IEnumerable<SszProperty> properties, string expressionPrefix)
    {
        foreach (SszProperty property in properties)
        {
            string validation = ValidationStatement(decl, property, $"{expressionPrefix}.{property.Name}");
            if (!string.IsNullOrEmpty(validation))
            {
                yield return validation;
            }
        }
    }

    private static string StaticFieldEncodeStatement(SszProperty property, int staticOffset, string containerExpr)
    {
        string destSpan = $"data.Slice({staticOffset}, {property.StaticLength})";
        string valueExpr = $"{containerExpr}.{property.Name}";
        string encodedValueExpr = EncodeValueExpression(property, valueExpr);

        string statement;
        if (property.Kind != Kind.Basic)
        {
            statement = EncodeStatement(destSpan, property, valueExpr);
        }
        else if (property.Type.EnumType is { HasCustomInlineCodec: true } enumType)
        {
            statement = enumType.CustomEncodeTemplate!
                .Replace("{0}", destSpan)
                .Replace("{1}", $"({enumType.TypeReferenceName}){encodedValueExpr}");
        }
        else if (property.Type.HasCustomInlineCodec)
        {
            statement = property.Type.CustomEncodeTemplate!
                .Replace("{0}", destSpan)
                .Replace("{1}", encodedValueExpr);
        }
        else
        {
            statement = property.HandledByStd
                ? $"Nethermind.Serialization.Ssz.Ssz.Encode({destSpan}, {encodedValueExpr});"
                : $"{property.Type.StaticMemberAccess}.Encode({destSpan}, {encodedValueExpr});";
        }

        // Nullable reference-typed static fields encode as zeros when null.
        return NullClearingEncodeStatement(destSpan, property, valueExpr, statement);
    }

    private static string NullClearingEncodeStatement(string target, SszProperty property, string valueExpr, string statement) =>
        property.IsNullable ? $"if ({valueExpr} is null) {target}.Clear(); else {statement}" : statement;

    private static bool IsByteList(SszProperty property) =>
        property.Kind == Kind.List && !property.IsSpanLikeProperty && property.Type is { Name: nameof(Byte), IsSszBasicType: true };

    private static bool IsByteVector(SszProperty property) =>
        property.Kind == Kind.Vector && !property.IsSpanLikeProperty && property.Type is { Name: nameof(Byte), IsSszBasicType: true };

    private static string DecodeAndAssign(SszType decl, SszProperty property, string sliceExpression)
    {
        string variableName = VarName(property.Name);

        if (property.Type.EnumType is { HasCustomInlineCodec: true } enumType && property.Kind == Kind.Basic)
        {
            string decodeStatement = $"container.{property.Name} = ({property.Type.TypeReferenceName}){enumType.CustomDecodeMethod}({sliceExpression});";
            string validation = ValidationStatement(decl, property, $"container.{property.Name}");
            return string.IsNullOrEmpty(validation) ? decodeStatement : $"{decodeStatement} {validation}";
        }

        if (property.Type.HasCustomInlineCodec && property.Kind == Kind.Basic)
        {
            string decodeStatement = property.Type.CustomDecodeTemplate!
                .Replace("{0}", sliceExpression)
                .Replace("{1}", $"container.{property.Name}");
            string validation = ValidationStatement(decl, property, $"container.{property.Name}");
            return string.IsNullOrEmpty(validation) ? decodeStatement : $"{decodeStatement} {validation}";
        }

        if (IsByteList(property))
        {
            string assignment = DecodeAssignmentExpression(property, variableName, sourceIsArray: true);
            return $"{{ byte[] {variableName} = DecodeSszByteList({sliceExpression}, {property.Limit}UL, nameof({decl.TypeReferenceName}), nameof({property.Name})); container.{property.Name} = {assignment}; }}";
        }

        if (IsByteVector(property))
        {
            string assignment = DecodeAssignmentExpression(property, variableName, sourceIsArray: true);
            return $"{{ byte[] {variableName} = DecodeSszByteVector({sliceExpression}, {property.Length}, nameof({decl.TypeReferenceName}), nameof({property.Name})); container.{property.Name} = {assignment}; }}";
        }

        if ((property.Kind is Kind.Vector or Kind.List or Kind.ProgressiveList) && property.Type.Kind == Kind.Basic && property.Type.HasCustomInlineCodec)
        {
            int itemSize = property.Type.StaticLength;
            string remainderGuard = property.Kind is Kind.List or Kind.ProgressiveList
                ? $"ValidateSszMultipleOf({sliceExpression}.Length, {itemSize}, \"{decl.TypeReferenceName}.{property.Name}\");"
                : string.Empty;
            string countDeclaration = property.Kind == Kind.Vector
                ? string.Empty
                : $"int __count = {sliceExpression}.Length / {itemSize};";
            string countExpression = property.Kind == Kind.Vector ? property.Length!.Value.ToString() : "__count";
            // No guard for limits beyond int.MaxValue: an int-typed count can never exceed them
            string limitGuard = (property.Kind == Kind.List && property.Limit is <= int.MaxValue)
                ? $"if (__count > {property.Limit.Value}) throw new System.IO.InvalidDataException($\"{decl.TypeReferenceName}.{property.Name}: list count {{__count}} exceeds SSZ limit {property.Limit.Value}\");"
                : string.Empty;
            string assignment = DecodeAssignmentExpression(property, variableName, sourceIsArray: true);
            string validation = ValidationStatement(decl, property, $"container.{property.Name}");
            string collectionDecode = $"{property.Type.TypeReferenceName}[] {variableName} = new {property.Type.TypeReferenceName}[{countExpression}]; {property.Type.CustomDecodeMethod}({sliceExpression}, {variableName});";
            string block = $"{{ {remainderGuard} {countDeclaration} {limitGuard} {collectionDecode} container.{property.Name} = {assignment}; }}";
            return string.IsNullOrEmpty(validation) ? block : $"{block} {validation}";
        }

        if ((property.Kind is Kind.Vector or Kind.List or Kind.ProgressiveList) && property.Type.Kind == Kind.Basic && property.Type.EnumType is { HasCustomInlineCodec: true } enumType2)
        {
            int itemSize = property.Type.StaticLength;
            string remainderGuard = property.Kind is Kind.List or Kind.ProgressiveList
                ? $"ValidateSszMultipleOf({sliceExpression}.Length, {itemSize}, \"{decl.TypeReferenceName}.{property.Name}\");"
                : string.Empty;
            string countDeclaration = property.Kind == Kind.Vector
                ? string.Empty
                : $"int __count = {sliceExpression}.Length / {itemSize};";
            string countExpression = property.Kind == Kind.Vector ? property.Length!.Value.ToString() : "__count";
            // No guard for limits beyond int.MaxValue: an int-typed count can never exceed them
            string limitGuard = (property.Kind == Kind.List && property.Limit is <= int.MaxValue)
                ? $"if (__count > {property.Limit.Value}) throw new System.IO.InvalidDataException($\"{decl.TypeReferenceName}.{property.Name}: list count {{__count}} exceeds SSZ limit {property.Limit.Value}\");"
                : string.Empty;
            string assignment = DecodeAssignmentExpression(property, variableName, sourceIsArray: true);
            string validation = ValidationStatement(decl, property, $"container.{property.Name}");
            string collectionDecode = $"{property.Type.TypeReferenceName}[] {variableName} = new {property.Type.TypeReferenceName}[{countExpression}]; {enumType2.CustomDecodeMethod}({sliceExpression}, System.Runtime.InteropServices.MemoryMarshal.Cast<{property.Type.TypeReferenceName}, {enumType2.TypeReferenceName}>({variableName}.AsSpan()));";
            string block = $"{{ {remainderGuard} {countDeclaration} {limitGuard} {collectionDecode} container.{property.Name} = {assignment}; }}";
            return string.IsNullOrEmpty(validation) ? block : $"{block} {validation}";
        }

        string outType = property.Kind switch
        {
            Kind.BitVector or Kind.BitList or Kind.ProgressiveBitList => "BitArray",
            _ when property.IsCollection => property.HandledByStd ? $"ReadOnlySpan<{property.Type.TypeReferenceName}>" : $"{property.Type.TypeReferenceName}[]",
            _ => property.Type.TypeReferenceName,
        };

        string decode = property.Kind switch
        {
            Kind.BitVector => $"DecodeSszBitVector({sliceExpression}, {property.Length}, out {outType} {variableName});",
            Kind.BitList or Kind.ProgressiveBitList => $"DecodeSszBitList({sliceExpression}, nameof({decl.TypeReferenceName}), out {outType} {variableName});",
            _ when property.HandledByStd => $"Nethermind.Serialization.Ssz.Ssz.Decode({sliceExpression}, out {outType} {variableName});",
            _ => $"{property.Type.StaticMemberAccess}.Decode({sliceExpression}, out {outType} {variableName});",
        };

        string assignment2 = $"container.{property.Name} = {DecodeAssignmentExpression(property, variableName, !property.HandledByStd)};";
        string validation2 = ValidationStatement(decl, property, $"container.{property.Name}");

        string preAllocationListGuard = string.Empty;
        // No guard for limits beyond int.MaxValue: an int-typed count can never exceed them
        if (property.Kind == Kind.List && property.Limit is <= int.MaxValue && !property.HandledByStd)
        {
            preAllocationListGuard = property.Type.IsVariable
                ? $"if ({sliceExpression}.Length >= {SszType.PointerLength}) {{ int __firstOffset = DecodeSszOffset({sliceExpression}.Slice(0, {SszType.PointerLength})); int __preCount = __firstOffset / {SszType.PointerLength}; if (__preCount > {property.Limit.Value}) throw new System.IO.InvalidDataException($\"{decl.TypeReferenceName}.{property.Name}: list count {{__preCount}} exceeds SSZ limit {property.Limit.Value}\"); }}"
                : $"{{ int __preCount = {sliceExpression}.Length / {property.Type.StaticLength}; if (__preCount > {property.Limit.Value}) throw new System.IO.InvalidDataException($\"{decl.TypeReferenceName}.{property.Name}: list count {{__preCount}} exceeds SSZ limit {property.Limit.Value}\"); }}";
        }

        List<string> statements = [];
        if (!string.IsNullOrEmpty(preAllocationListGuard))
        {
            statements.Add(preAllocationListGuard);
        }
        statements.Add(decode);
        statements.Add(assignment2);
        if (!string.IsNullOrEmpty(validation2))
        {
            statements.Add(validation2);
        }
        return string.Join(" ", statements);
    }

    private static string DecodeAssignmentExpression(SszProperty property, string variableName, bool sourceIsArray) =>
        property switch
        {
            { IsReadOnlySpanProperty: true } => variableName,
            { IsReadOnlyMemoryProperty: true } => sourceIsArray ? variableName : $"{variableName}.ToArray()",
            { IsSpanLikeProperty: true, HandledByStd: true } => sourceIsArray ? variableName : $"{variableName}.ToArray()",
            { IsSpanLikeProperty: true } => variableName,
            { IsMemoryLikeProperty: true } => sourceIsArray ? variableName : $"{variableName}.ToArray()",
            { IsCollection: true, CanConstructCollectionFromReadOnlySpan: true, CollectionTypeReferenceName: not null } => $"new {property.CollectionTypeReferenceName}({variableName})",
            { IsCollection: true, IsArrayProperty: true } => sourceIsArray ? variableName : $"[ ..{variableName}]",
            { IsCollection: true } => $"[ ..{variableName}]",
            _ => variableName,
        };

    private static string ConverterMerkleizeStatement(SszProperty property, string expression, string rootName) =>
        $"MerkleizeWithConverter({expression}, {property.Type.CustomFeedMethod}, out {rootName});";

    private static string ConverterFeedStatement(SszProperty property, string expression) =>
        property.Type.CustomFeedTemplate!
            .Replace("{0}", "merkleizer")
            .Replace("{1}", expression);

