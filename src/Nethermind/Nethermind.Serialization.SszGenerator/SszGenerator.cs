    internal static void MerkleizeProgressiveBitList(BitArray? value, out UInt256 root)
    {
        BitArray bits = value ?? new BitArray(0);
        int byteLength = (bits.Length + 7) / 8;
        // BitArray.CopyTo requires a byte[] target, so we rent regardless of size;
        // the small-chunkCount fast path in MerkleizeProgressiveBytes covers stackalloc.
        byte[] bytes = System.Buffers.ArrayPool<byte>.Shared.Rent(byteLength);
        try
        {
            Array.Clear(bytes, 0, byteLength);
            bits.CopyTo(bytes, 0);
            MerkleizeProgressiveBytes(bytes.AsSpan(0, byteLength), out root);
        }
        finally
        {
            System.Buffers.ArrayPool<byte>.Shared.Return(bytes);
        }
        Merkle.MixIn(ref root, bits.Length);
    }

    internal delegate void FeedItem<T>(ref Merkleizer merkleizer, T item);

    internal enum ConverterCollectionMerkleKind { Vector, List, ProgressiveList }

    private static void MerkleizeCompositeCollectionWithConverter<T>(
        ReadOnlySpan<T> items, FeedItem<T> feed,
        ConverterCollectionMerkleKind kind, ulong limit, out UInt256 root)
    {
        int count = items.Length;
        UInt256[] subRoots = System.Buffers.ArrayPool<UInt256>.Shared.Rent(count);
        try
        {
            for (int i = 0; i < count; i++)
            {
                MerkleizeWithConverter(items[i], feed, out subRoots[i]);
            }
            ReadOnlySpan<UInt256> active = subRoots.AsSpan(0, count);
            switch (kind)
            {
                case ConverterCollectionMerkleKind.Vector: Merkle.Merkleize(out root, active); break;
                case ConverterCollectionMerkleKind.List: Merkle.Merkleize(out root, active, limit); break;
                default: Merkle.MerkleizeProgressive(out root, active); break;
            }
        }
        finally
        {
            System.Buffers.ArrayPool<UInt256>.Shared.Return(subRoots);
        }
        if (kind != ConverterCollectionMerkleKind.Vector) Merkle.MixIn(ref root, count);
    }

    internal static void MerkleizeCompositeVectorWithConverter<T>(ReadOnlySpan<T> items, ulong length, FeedItem<T> feed, out UInt256 root)
    {
        Debug.Assert((ulong)items.Length == length, "Vector items count must equal declared length");
        MerkleizeCompositeCollectionWithConverter(items, feed, ConverterCollectionMerkleKind.Vector, limit: 0, out root);
    }

    internal static void MerkleizeDefaultWithConverter<T>(int itemSize, DecodeItem<T> decode, FeedItem<T> feed, out UInt256 root)
    {
        const int StackByteLimit = 256;
        scoped Span<byte> bytes;
        byte[]? rented = null;
        if (itemSize <= StackByteLimit)
        {
            Span<byte> stack = stackalloc byte[StackByteLimit];
            bytes = stack[..itemSize];
        }
        else
        {
            rented = System.Buffers.ArrayPool<byte>.Shared.Rent(itemSize);
            bytes = rented.AsSpan(0, itemSize);
        }
        try
        {
            bytes.Clear();
            MerkleizeWithConverter(decode(bytes), feed, out root);
        }
        finally
        {
            if (rented is not null) System.Buffers.ArrayPool<byte>.Shared.Return(rented);
        }
    }

    internal static void MerkleizeDefaultCompositeVectorWithConverter<T>(int itemSize, ulong length, DecodeItem<T> decode, FeedItem<T> feed, out UInt256 root)
    {
        if (length == 0)
        {
            root = UInt256.Zero;
            return;
        }

        MerkleizeDefaultWithConverter(itemSize, decode, feed, out UInt256 itemRoot);
        Merkleizer merkleizer = new(Merkle.NextPowerOfTwoExponent(length));
        for (ulong i = 0; i < length; i++)
        {
            merkleizer.Feed(itemRoot);
        }

        merkleizer.CalculateRoot(out root);
    }

    internal static void MerkleizeCompositeListWithConverter<T>(ReadOnlySpan<T> items, ulong limit, FeedItem<T> feed, out UInt256 root)
        => MerkleizeCompositeCollectionWithConverter(items, feed, ConverterCollectionMerkleKind.List, limit, out root);

    internal static void MerkleizeCompositeProgressiveListWithConverter<T>(ReadOnlySpan<T> items, FeedItem<T> feed, out UInt256 root)
        => MerkleizeCompositeCollectionWithConverter(items, feed, ConverterCollectionMerkleKind.ProgressiveList, limit: 0, out root);

    internal static void MerkleizeWithConverter<T>(T value, FeedItem<T> feed, out UInt256 root)
    {
        Span<UInt256> chunk = stackalloc UInt256[1];
        Merkleizer merkleizer = new(chunk);
        feed(ref merkleizer, value);
        root = chunk[0];
    }
}
