            // rejected at parse time (a BitArray cannot exceed int.MaxValue bits), so this fits.
            arguments += $", {property.Limit!.Value}";
        }
        else if (property.Kind == Kind.ProgressiveBitList)
        {
            arguments += $", {UnboundedBitlistLimit}";
        }

        return $"{(property.HandledByStd ? "Nethermind.Serialization.Ssz.Ssz.Encode" : $"{property.Type.StaticMemberAccess}.Encode")}({arguments});";
    }

    private static bool RequiresNullGuard(SszProperty property)
    {
        if (property.IsSpanLikeProperty || property.IsMemoryLikeProperty || property.Kind is Kind.BitList or Kind.ProgressiveBitList)
        {
            return false;
        }

        return (property.IsNullable && CanEncodeNullAsDefault(property)) || !property.Type.IsStruct;
    }

    private static string UsingsBlock(SszType decl) =>
        string.Join("\n", ReferencedTypes(decl)
            .SelectMany(GeneratedNamespaces)
            .Concat(decl.TypeParameterConstraintNamespaces)
            .Append("Nethermind.Int256")
            .Append("System")
            .Distinct()
            .OrderBy(x => x)
            .Where(x => !string.IsNullOrEmpty(x) && x != decl.Namespace && x != "Nethermind.Serialization.Ssz")
            .Select(n => $"using {n};"));

    private static IEnumerable<SszType> ReferencedTypes(SszType type)
    {
        HashSet<SszType> visited = [];
        Stack<SszType> pending = new();
        pending.Push(type);

        while (pending.Count > 0)
        {
            SszType current = pending.Pop();
            if (!visited.Add(current))
            {
                continue;
            }

            yield return current;

            if (current.EnumType is not null)
            {
                pending.Push(current.EnumType);
            }

            if (current.Members is null)
            {
                continue;
            }

            for (int i = 0; i < current.Members.Length; i++)
            {
                pending.Push(current.Members[i].Type);
            }
        }
    }

    private static IEnumerable<string> GeneratedNamespaces(SszType type)
    {
        string? typeNamespace = type.Namespace;
        if (typeNamespace is { Length: > 0 })
        {
            yield return typeNamespace;
        }

        foreach (string namespaceName in type.AdditionalNamespaces)
        {
            yield return namespaceName;
        }
    }

