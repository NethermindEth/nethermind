    private static string MerkleizeDefaultStaticRootStatement(SszProperty property, string rootName)
    {
        string valueName = "__default" + property.Name;
        return $"{{ byte[] {valueName}Data = new byte[{property.StaticLength}]; {property.Type.StaticMemberAccess}.Decode({valueName}Data, out {property.Type.TypeReferenceName} {valueName}); {property.Type.StaticMemberAccess}.Merkleize({valueName}, out {rootName}); }}";
    }

    private static string MerkleizeRootAssignmentStatement(SszProperty property, string expression, string rootName)
    {
        string body = MerkleizeRootStatement(property, expression, rootName);
        if (!property.IsNullable || property.IsSpanLikeProperty || property.Kind is Kind.BitList or Kind.ProgressiveBitList)
        {
            return body;
        }

        if (property.Kind is (Kind.Vector or Kind.BitVector or Kind.List or Kind.ProgressiveList) && CanEncodeNullAsDefault(property))
        {
            return $"if ({expression} is null) {{ {MerkleizeEmptyCollectionRootStatement(property, rootName)} }} else {{ {body} }}";
        }

        if (property.Kind == Kind.Basic && property.Type.HasCustomInlineCodec)
        {
            return $"if ({expression} is null) {{ MerkleizeDefaultWithConverter<{property.Type.TypeReferenceName}>({property.Type.StaticLength}, {property.Type.CustomDecodeMethod}, {property.Type.CustomFeedMethod}, out {rootName}); }} else {{ {body} }}";
        }

        if (!property.IsVariable && property.Kind != Kind.Basic)
        {
            return $"if ({expression} is null) {{ {MerkleizeDefaultStaticRootStatement(property, rootName)} }} else {{ {body} }}";
        }

        return $"if ({expression} is null) {{ {rootName} = default; }} else {{ {body} }}";
    }

    private static string MerkleizeFeedStatement(SszProperty property, string expression)
    {
        if (property.Kind == Kind.Basic && property.Type.CustomFeedTemplate is not null && !property.IsNullable)
        {
            return ConverterFeedStatement(property, expression);
        }

        string rootName = VarName(property.Name) + "Root";
        return $"UInt256 {rootName}; {MerkleizeRootAssignmentStatement(property, expression, rootName)} merkleizer.Feed({rootName});";
    }

    private static string DynamicLength(SszType container, SszProperty m)
    {
        if ((m.Kind & (Kind.Collection | Kind.BitList | Kind.ProgressiveBitList)) != Kind.None && m.Type.Kind == Kind.Basic)
        {
            string length = m.Kind is Kind.BitList or Kind.ProgressiveBitList
                ? $"(container.{m.Name} is not null ? container.{m.Name}.Length / 8 + 1 : 1)"
                : $"({m.Type.StaticLength} * {SpanExpression(m, $"container.{m.Name}")}.Length)";
            return m.IsNullable && (m.Kind is Kind.List or Kind.ProgressiveList) && CanEncodeNullAsDefault(m)
                ? $"(container.{m.Name} is null ? 0 : {length})"
                : length;
        }

        string call = m.IsCollection
            ? $"{m.Type.StaticMemberAccess}.GetLength({SpanExpression(m, $"container.{m.Name}")})"
            : $"{m.Type.StaticMemberAccess}.GetLength(container.{m.Name})";

        // A null reference-typed property contributes zero bytes to the variable-length region —
        // matches the static-field encode's "skip on null" behaviour and keeps offsets consistent.
        return m.IsNullable ? $"(container.{m.Name} is null ? 0 : {call})" : call;
    }

    private static string ByteArrayLiteral(byte[] value) => $"[{string.Join(", ", value)}]";

    private static string ProgressiveContainerMerkleizeBody(SszType decl)
    {
        string activeFields = ByteArrayLiteral(decl.ActiveFieldsBytes ?? []);
        IEnumerable<string> memberRoots = decl.Members!.Select((m, index) =>
        {
            string validation = ValidationStatement(decl, m, $"container.{m.Name}");
            string merkleize = MerkleizeRootAssignmentStatement(m, $"container.{m.Name}", $"subRoots[{index}]");
            return string.IsNullOrEmpty(validation) ? merkleize : $"{validation} {merkleize}";
        });

        return string.Join("\n",
        [
            $"UInt256[] subRoots = new UInt256[{decl.Members!.Length}];",
            ..memberRoots,
            "Merkle.MerkleizeProgressive(out root, subRoots);",
            $"Merkle.MixInActiveFields(ref root, {activeFields});",
        ]);
    }

    private static string EncodeStatement(string target, SszProperty property, string expression)
    {
        if (property.Kind == Kind.Basic && property.Type.HasCustomInlineCodec)
        {
            return property.Type.CustomEncodeTemplate!
                .Replace("{0}", target)
                .Replace("{1}", EncodeValueExpression(property, expression));
        }

        if ((property.Kind is Kind.List or Kind.Vector or Kind.ProgressiveList) && property.Type.Kind == Kind.Basic && property.Type.HasCustomInlineCodec)
        {
            return $"{property.Type.CustomEncodeMethod}({target}, {SpanExpression(property, expression)});";
        }

        if ((property.Kind is Kind.List or Kind.Vector or Kind.ProgressiveList) && property.Type.Kind == Kind.Basic && property.Type.EnumType is { HasCustomInlineCodec: true } enumType)
        {
            return $"{enumType.CustomEncodeMethod}({target}, {EnumSpanExpression(property, expression)});";
        }

        string arguments = $"{target}, {EncodeValueExpression(property, expression)}";
        if (property.Kind == Kind.BitList)
        {
