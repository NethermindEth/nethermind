using System;
using System.Collections.Immutable;
using System.Collections.Generic;
using System.IO;
using System.Text;
using System.Threading;
using Microsoft.CodeAnalysis;
using Microsoft.CodeAnalysis.CSharp;

namespace Nethermind.JsonRpc.SourceGenerator.Build;

internal static class Program
{
    private const string GeneratedSourceHeader = "// <auto-generated/>\n#nullable enable\n";

    private static int Main(string[] args)
    {
        try
        {
            Arguments arguments = Arguments.Parse(args);
            Generate(arguments);
            return 0;
        }
        catch (Exception exception)
        {
            Console.Error.WriteLine(exception);
            return 1;
        }
    }

    private static void Generate(Arguments arguments)
    {
        string[] sourcePaths = ReadExistingLines(arguments.SourcesFile);
        string[] referencePaths = ReadExistingLines(arguments.ReferencesFile);
        CSharpParseOptions parseOptions = CSharpParseOptions.Default
            .WithLanguageVersion(LanguageVersion.Preview)
            .WithPreprocessorSymbols(GetPreprocessorSymbols(arguments.DefineConstants));

        SyntaxTree[] syntaxTrees = new SyntaxTree[sourcePaths.Length];
        for (int i = 0; i < sourcePaths.Length; i++)
        {
            syntaxTrees[i] = CSharpSyntaxTree.ParseText(
                File.ReadAllText(sourcePaths[i]),
                parseOptions,
                sourcePaths[i]);
        }

        MetadataReference[] references = new MetadataReference[referencePaths.Length];
        for (int i = 0; i < referencePaths.Length; i++)
        {
            references[i] = MetadataReference.CreateFromFile(referencePaths[i]);
        }

        CSharpCompilation compilation = CSharpCompilation.Create(
            arguments.AssemblyName,
            syntaxTrees,
            references,
            new CSharpCompilationOptions(OutputKind.DynamicallyLinkedLibrary));

        ImmutableArray<ITypeSymbol> typeSymbols = CollectTypeSymbols(compilation, syntaxTrees);
        string[] sortedTypes = GetSourceGeneratedTypes(typeSymbols, compilation.Assembly);
        if (sortedTypes.Length == 0)
        {
            if (File.Exists(arguments.OutputPath))
            {
                File.Delete(arguments.OutputPath);
            }

            return;
        }

        string output = GenerateContextSource(sortedTypes);
        Directory.CreateDirectory(Path.GetDirectoryName(arguments.OutputPath)!);
        WriteIfChanged(arguments.OutputPath, output);
    }

    private static ImmutableArray<ITypeSymbol> CollectTypeSymbols(CSharpCompilation compilation, SyntaxTree[] syntaxTrees)
    {
        ImmutableArray<ITypeSymbol>.Builder types = ImmutableArray.CreateBuilder<ITypeSymbol>();
        for (int i = 0; i < syntaxTrees.Length; i++)
        {
            SemanticModel semanticModel = compilation.GetSemanticModel(syntaxTrees[i]);
            SyntaxNode root = syntaxTrees[i].GetRoot();
            foreach (SyntaxNode node in root.DescendantNodes())
            {
                ImmutableArray<ITypeSymbol> jsonTypes = RpcJsonTypeDiscovery.GetJsonTypeSymbols(semanticModel, node, CancellationToken.None);
                if (!jsonTypes.IsDefaultOrEmpty)
                {
                    types.AddRange(jsonTypes);
                }
            }
        }

        return types.ToImmutable();
    }

    private static string GenerateContextSource(string[] sortedTypes)
    {
        StringBuilder builder = new();
        builder.AppendLine(GeneratedSourceHeader);
        builder.AppendLine(
            """
            using System.Runtime.CompilerServices;
            using System.Text.Json.Serialization;
            using Nethermind.Serialization.Json;

            namespace Nethermind.JsonRpc;

            [JsonSourceGenerationOptions(
                GenerationMode = JsonSourceGenerationMode.Metadata,
                PropertyNamingPolicy = JsonKnownNamingPolicy.CamelCase,
                PropertyNameCaseInsensitive = true,
                DefaultIgnoreCondition = JsonIgnoreCondition.WhenWritingNull,
                IncludeFields = true)]
            """);
        for (int i = 0; i < sortedTypes.Length; i++)
        {
            builder.Append("[JsonSerializable(typeof(");
            builder.Append(sortedTypes[i]);
            builder.AppendLine("))]");
        }

        builder.AppendLine(
            """
            internal partial class GeneratedRpcJsonContext : JsonSerializerContext
            {
                [ModuleInitializer]
                internal static void Register()
                {
                    EthereumJsonSerializer.AddTypeInfoResolver(Default, JsonTypeInfoResolverPriority.GeneratedRpc);
                }
            }
            """);
        return builder.ToString();
    }

    private static string[] GetSourceGeneratedTypes(ImmutableArray<ITypeSymbol> typeSymbols, IAssemblySymbol currentAssembly)
    {
        JsonContextEligibility eligibility = new(currentAssembly);
        Dictionary<string, JsonTypeCandidate> candidatesByDisplayName = new(StringComparer.Ordinal);

        for (int i = 0; i < typeSymbols.Length; i++)
        {
            ITypeSymbol type = typeSymbols[i];
            string displayName = RpcJsonTypeDiscovery.GetTypeDisplayString(type);
            if (!candidatesByDisplayName.ContainsKey(displayName) &&
                eligibility.TryCreateCandidate(type, displayName, out JsonTypeCandidate? candidate) &&
                candidate is not null)
            {
                candidatesByDisplayName.Add(displayName, candidate);
            }
        }

        HashSet<string> collidingGeneratedTypeNames = GetCollidingGeneratedTypeNames(candidatesByDisplayName.Values);
        List<string> filteredTypes = new(candidatesByDisplayName.Count);
        foreach (JsonTypeCandidate candidate in candidatesByDisplayName.Values)
        {
            if (!candidate.UsesAnyGeneratedTypeName(collidingGeneratedTypeNames))
            {
                filteredTypes.Add(candidate.DisplayName);
            }
        }

        string[] sortedTypes = [.. filteredTypes];
        Array.Sort(sortedTypes, StringComparer.Ordinal);
        return sortedTypes;
    }

    private static HashSet<string> GetCollidingGeneratedTypeNames(IEnumerable<JsonTypeCandidate> candidates)
    {
        Dictionary<string, string> ownerByGeneratedName = new(StringComparer.Ordinal);
        HashSet<string> collisions = new(StringComparer.Ordinal);

        foreach (JsonTypeCandidate candidate in candidates)
        {
            foreach (KeyValuePair<string, string> generatedTypeName in candidate.GeneratedTypeNames)
            {
                if (ownerByGeneratedName.TryGetValue(generatedTypeName.Key, out string? existingOwner))
                {
                    if (existingOwner != generatedTypeName.Value)
                    {
                        collisions.Add(generatedTypeName.Key);
                    }
                }
                else
                {
                    ownerByGeneratedName.Add(generatedTypeName.Key, generatedTypeName.Value);
                }
            }
        }

        return collisions;
    }

    private sealed record JsonTypeCandidate(string DisplayName, Dictionary<string, string> GeneratedTypeNames)
    {
        public bool UsesAnyGeneratedTypeName(HashSet<string> generatedTypeNames) =>
            generatedTypeNames.Overlaps(GeneratedTypeNames.Keys);
    }

    private sealed class JsonContextEligibility(IAssemblySymbol currentAssembly)
    {
        private const string JsonSerializationNamespace = "System.Text.Json.Serialization";
        private const string SystemNamespace = "System";

        private readonly IAssemblySymbol _currentAssembly = currentAssembly;

        // The context is emitted into the target assembly, so roots that require inaccessible
        // converters or colliding generated metadata names must stay on the existing resolver path.
        public bool TryCreateCandidate(
            ITypeSymbol type,
            string displayName,
            out JsonTypeCandidate? candidate)
        {
            if (!IsRootOwnedByCurrentAssembly(type))
            {
                candidate = null;
                return false;
            }

            Dictionary<string, string> generatedTypeNames = new(StringComparer.Ordinal);
            HashSet<string> visitedTypes = new(StringComparer.Ordinal);
            if (!CanGenerateMetadata(type, generatedTypeNames, visitedTypes))
            {
                candidate = null;
                return false;
            }

            candidate = new JsonTypeCandidate(displayName, generatedTypeNames);
            return true;
        }

        private bool IsRootOwnedByCurrentAssembly(ITypeSymbol type)
        {
            if (type is IArrayTypeSymbol arrayType)
            {
                return IsRootOwnedByCurrentAssembly(arrayType.ElementType);
            }

            if (type is not INamedTypeSymbol namedType)
            {
                return false;
            }

            if (namedType.SpecialType != SpecialType.None ||
                IsSystemType(namedType))
            {
                return namedType.TypeArguments.Length == 0 || HasCurrentAssemblyTypeArgument(namedType);
            }

            if (SymbolEqualityComparer.Default.Equals(namedType.ContainingAssembly, _currentAssembly))
            {
                return true;
            }

            return namedType.IsGenericType && HasCurrentAssemblyTypeArgument(namedType);
        }

        private bool HasCurrentAssemblyTypeArgument(INamedTypeSymbol type)
        {
            for (int i = 0; i < type.TypeArguments.Length; i++)
            {
                if (IsCurrentAssemblyType(type.TypeArguments[i]))
                {
                    return true;
                }
            }

            return false;
        }

        private bool IsCurrentAssemblyType(ITypeSymbol type)
        {
            if (type is IArrayTypeSymbol arrayType)
            {
                return IsCurrentAssemblyType(arrayType.ElementType);
            }

            if (type is not INamedTypeSymbol namedType)
            {
                return false;
            }

            if (SymbolEqualityComparer.Default.Equals(namedType.ContainingAssembly, _currentAssembly))
            {
                return true;
            }

            for (int i = 0; i < namedType.TypeArguments.Length; i++)
            {
                if (IsCurrentAssemblyType(namedType.TypeArguments[i]))
                {
                    return true;
                }
            }

            return false;
        }

        private bool CanGenerateMetadata(
            ITypeSymbol type,
            Dictionary<string, string> generatedTypeNames,
            HashSet<string> visitedTypes)
        {
            if (type.TypeKind is TypeKind.Error or TypeKind.TypeParameter or TypeKind.Pointer or TypeKind.FunctionPointer ||
                type.SpecialType == SpecialType.System_Void)
            {
                return false;
            }

            string displayName = RpcJsonTypeDiscovery.GetTypeDisplayString(type);
            if (!visitedTypes.Add(displayName))
            {
                return true;
            }

            if (!AddGeneratedTypeName(type, generatedTypeNames) ||
                !HasOnlyAccessibleJsonConverterAttributes(type))
            {
                return false;
            }

            if (type is IArrayTypeSymbol arrayType)
            {
                return CanGenerateMetadata(arrayType.ElementType, generatedTypeNames, visitedTypes);
            }

            if (type is not INamedTypeSymbol namedType)
            {
                return true;
            }

            for (int i = 0; i < namedType.TypeArguments.Length; i++)
            {
                if (!CanGenerateMetadata(namedType.TypeArguments[i], generatedTypeNames, visitedTypes))
                {
                    return false;
                }
            }

            if (!AddJsonDerivedTypes(namedType, generatedTypeNames, visitedTypes) ||
                !ShouldInspectMembers(namedType))
            {
                return true;
            }

            ImmutableArray<ISymbol> members = namedType.GetMembers();
            for (int i = 0; i < members.Length; i++)
            {
                if (!CanGenerateMemberMetadata(members[i], generatedTypeNames, visitedTypes))
                {
                    return false;
                }
            }

            return true;
        }

        private bool AddJsonDerivedTypes(
            INamedTypeSymbol type,
            Dictionary<string, string> generatedTypeNames,
            HashSet<string> visitedTypes)
        {
            ImmutableArray<AttributeData> attributes = type.GetAttributes();
            for (int i = 0; i < attributes.Length; i++)
            {
                AttributeData attribute = attributes[i];
                if (!IsAttribute(attribute.AttributeClass, JsonSerializationNamespace, "JsonDerivedTypeAttribute") ||
                    attribute.ConstructorArguments.Length == 0 ||
                    attribute.ConstructorArguments[0].Value is not ITypeSymbol derivedType)
                {
                    continue;
                }

                if (!CanGenerateMetadata(derivedType, generatedTypeNames, visitedTypes))
                {
                    return false;
                }
            }

            return true;
        }

        private bool CanGenerateMemberMetadata(
            ISymbol member,
            Dictionary<string, string> generatedTypeNames,
            HashSet<string> visitedTypes)
        {
            if (!HasOnlyAccessibleJsonConverterAttributes(member))
            {
                return false;
            }

            if (member is IPropertySymbol property)
            {
                if (property.IsStatic ||
                    property.Parameters.Length != 0 ||
                    !IsJsonVisible(property))
                {
                    return true;
                }

                return CanGenerateMetadata(property.Type, generatedTypeNames, visitedTypes);
            }

            if (member is IFieldSymbol field)
            {
                if (field.IsStatic ||
                    field.IsConst ||
                    !IsJsonVisible(field))
                {
                    return true;
                }

                return CanGenerateMetadata(field.Type, generatedTypeNames, visitedTypes);
            }

            return true;
        }

        private static bool IsJsonVisible(IPropertySymbol property) =>
            property.DeclaredAccessibility == Accessibility.Public ||
            property.GetMethod?.DeclaredAccessibility == Accessibility.Public ||
            property.SetMethod?.DeclaredAccessibility == Accessibility.Public ||
            HasAttribute(property, JsonSerializationNamespace, "JsonIncludeAttribute");

        private static bool IsJsonVisible(IFieldSymbol field) =>
            field.DeclaredAccessibility == Accessibility.Public ||
            HasAttribute(field, JsonSerializationNamespace, "JsonIncludeAttribute");

        private static bool ShouldInspectMembers(INamedTypeSymbol type) =>
            type.SpecialType == SpecialType.None &&
            !IsSystemType(type) &&
            type.TypeKind is TypeKind.Class or TypeKind.Struct or TypeKind.Interface;

        private bool HasOnlyAccessibleJsonConverterAttributes(ISymbol symbol)
        {
            ImmutableArray<AttributeData> attributes = symbol.GetAttributes();
            for (int i = 0; i < attributes.Length; i++)
            {
                AttributeData attribute = attributes[i];
                if (!IsAttribute(attribute.AttributeClass, JsonSerializationNamespace, "JsonConverterAttribute"))
                {
                    continue;
                }

                if (attribute.ConstructorArguments.Length == 0 ||
                    attribute.ConstructorArguments[0].Value is not INamedTypeSymbol converterType)
                {
                    continue;
                }

                if (!IsAccessibleFromGeneratedContext(converterType))
                {
                    return false;
                }
            }

            return true;
        }

        private bool IsAccessibleFromGeneratedContext(INamedTypeSymbol type)
        {
            INamedTypeSymbol? current = type;
            while (current is not null)
            {
                bool sameAssembly = SymbolEqualityComparer.Default.Equals(current.ContainingAssembly, _currentAssembly);
                if (current.DeclaredAccessibility == Accessibility.Public ||
                    sameAssembly && current.DeclaredAccessibility is Accessibility.Internal or Accessibility.ProtectedOrInternal)
                {
                    current = current.ContainingType;
                    continue;
                }

                return false;
            }

            return true;
        }

        private static bool AddGeneratedTypeName(ITypeSymbol type, Dictionary<string, string> generatedTypeNames)
        {
            string generatedTypeName = GetGeneratedTypeName(type);
            string displayName = RpcJsonTypeDiscovery.GetTypeDisplayString(type);
            if (generatedTypeNames.TryGetValue(generatedTypeName, out string? existingDisplayName))
            {
                return existingDisplayName == displayName;
            }

            generatedTypeNames.Add(generatedTypeName, displayName);
            return true;
        }

        private static string GetGeneratedTypeName(ITypeSymbol type)
        {
            if (type is IArrayTypeSymbol arrayType)
            {
                return GetGeneratedTypeName(arrayType.ElementType) + "Array";
            }

            if (type is INamedTypeSymbol namedType)
            {
                return GetNamedGeneratedTypeName(namedType);
            }

            return type.MetadataName;
        }

        private static string GetNamedGeneratedTypeName(INamedTypeSymbol type)
        {
            StringBuilder builder = new();
            if (type.ContainingType is not null)
            {
                builder.Append(GetNamedGeneratedTypeName(type.ContainingType));
            }

            string metadataName = type.MetadataName;
            int genericTickIndex = metadataName.IndexOf('`', StringComparison.Ordinal);
            builder.Append(genericTickIndex >= 0 ? metadataName[..genericTickIndex] : metadataName);

            if (type.IsGenericType)
            {
                for (int i = 0; i < type.TypeArguments.Length; i++)
                {
                    builder.Append(GetGeneratedTypeName(type.TypeArguments[i]));
                }
            }

            return builder.ToString();
        }

        private static bool HasNamespacePrefix(INamespaceSymbol namespaceSymbol, string namespaceName)
        {
            string fullName = namespaceSymbol.ToDisplayString();
            return fullName == namespaceName || fullName.StartsWith(namespaceName + ".", StringComparison.Ordinal);
        }

        private static bool IsSystemType(INamedTypeSymbol type) =>
            HasNamespacePrefix(type.ContainingNamespace, SystemNamespace);

        private static bool HasAttribute(ISymbol symbol, string namespaceName, string metadataName)
        {
            ImmutableArray<AttributeData> attributes = symbol.GetAttributes();
            for (int i = 0; i < attributes.Length; i++)
            {
                if (IsAttribute(attributes[i].AttributeClass, namespaceName, metadataName))
                {
                    return true;
                }
            }

            return false;
        }

        private static bool IsAttribute(INamedTypeSymbol? attributeClass, string namespaceName, string metadataName) =>
            attributeClass is not null &&
            attributeClass.ContainingNamespace.ToDisplayString() == namespaceName &&
            attributeClass.MetadataName == metadataName;
    }

    private static string[] GetPreprocessorSymbols(string defineConstants)
    {
        if (string.IsNullOrWhiteSpace(defineConstants))
        {
            return [];
        }

        string[] parts = defineConstants.Split([';', ','], StringSplitOptions.RemoveEmptyEntries | StringSplitOptions.TrimEntries);
        List<string> symbols = new(parts.Length);
        for (int i = 0; i < parts.Length; i++)
        {
            string part = parts[i];
            int equalsIndex = part.IndexOf('=');
            symbols.Add(equalsIndex >= 0 ? part[..equalsIndex] : part);
        }

        return [.. symbols];
    }

    private static string[] ReadExistingLines(string path)
    {
        if (!File.Exists(path))
        {
            return [];
        }

        string[] lines = File.ReadAllLines(path);
        List<string> existing = new(lines.Length);
        for (int i = 0; i < lines.Length; i++)
        {
            string line = lines[i];
            if (!string.IsNullOrWhiteSpace(line) && File.Exists(line))
            {
                existing.Add(line);
            }
        }

        return [.. existing];
    }

    private static void WriteIfChanged(string path, string content)
    {
        if (File.Exists(path) && File.ReadAllText(path) == content)
        {
            return;
        }

        File.WriteAllText(path, content, Encoding.UTF8);
    }

    private sealed record Arguments(
        string OutputPath,
        string SourcesFile,
        string ReferencesFile,
        string AssemblyName,
        string DefineConstants)
    {
        public static Arguments Parse(string[] args)
        {
            Dictionary<string, string> values = new(StringComparer.Ordinal);
            for (int i = 0; i < args.Length; i += 2)
            {
                if (i + 1 >= args.Length || !args[i].StartsWith("--", StringComparison.Ordinal))
                {
                    throw new ArgumentException($"Invalid argument at index {i}.");
                }

                values[args[i]] = args[i + 1];
            }

            return new Arguments(
                GetRequired(values, "--output"),
                GetRequired(values, "--sources"),
                GetRequired(values, "--references"),
                GetRequired(values, "--assembly-name"),
                values.GetValueOrDefault("--define-constants", string.Empty));
        }

        private static string GetRequired(Dictionary<string, string> values, string name)
        {
            if (values.TryGetValue(name, out string? value) && !string.IsNullOrWhiteSpace(value))
            {
                return value;
            }

            throw new ArgumentException($"Missing required argument {name}.");
        }
    }
